export const validateName = (nombre: string): string | null => {
  if (!nombre) return 'El nombre es requerido';
  if (nombre.length > 50) return 'El nombre no puede exceder los 50 caracteres';
  if (!/^[a-zA-ZáéíóúÁÉÍÓÚñÑ\s]+$/.test(nombre)) {
    return 'El nombre solo puede contener letras';
  }
  return null;
};

export const validateEmail = (email: string): string | null => {
  if (!email) return 'El email es requerido';
  if (email.length > 30) return 'El email no puede exceder los 30 caracteres';
  if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
    return 'Email inválido';
  }
  return null;
};

export const validateAge = (fechaNacimiento: string): string | null => {
  if (!fechaNacimiento) {
    return 'La fecha de nacimiento es requerida';
  }
  
  // Convertir la fecha a medianoche en la zona horaria local
  const inputDate = new Date(fechaNacimiento);
  const birthDate = new Date(inputDate.getFullYear(), inputDate.getMonth(), inputDate.getDate());
  
  // Obtener la fecha actual a medianoche en la zona horaria local
  const today = new Date();
  const currentDate = new Date(today.getFullYear(), today.getMonth(), today.getDate());
  
  let age = currentDate.getFullYear() - birthDate.getFullYear();
  const monthDiff = currentDate.getMonth() - birthDate.getMonth();
  
  if (monthDiff < 0 || (monthDiff === 0 && currentDate.getDate() < birthDate.getDate())) {
    age--;
  }
  
  if (age < 18) {
    return 'Debes ser mayor de 18 años para registrarte';
  }
  
  if (age > 99) {
    return 'La edad máxima permitida es 99 años';
  }
  
  return null;
};

export const validatePhone = (telefono: string): string | null => {
  if (!telefono) return 'El teléfono es requerido';
  if (!/^\d{9}$/.test(telefono)) {
    return 'El teléfono debe tener exactamente 9 dígitos';
  }
  return null;
};